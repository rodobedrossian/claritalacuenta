import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Loader2 } from "lucide-react";
import { Capacitor } from "@capacitor/core";
import { lovable } from "@/integrations/lovable";
import { toast } from "sonner";

/** Web URL para OAuth redirect en iOS Capacitor (capacitor://localhost no es válido en Lovable/Google) */
const WEB_APP_URL = import.meta.env.VITE_APP_URL || "https://claritalacuenta.lovable.app";

const GoogleIcon = () => (
  <svg viewBox="0 0 24 24" className="h-5 w-5" aria-hidden="true">
    <path
      d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
      fill="#4285F4"
    />
    <path
      d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
      fill="#34A853"
    />
    <path
      d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
      fill="#FBBC05"
    />
    <path
      d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
      fill="#EA4335"
    />
  </svg>
);

interface GoogleSignInButtonProps {
  onSuccess?: () => void;
}

export default function GoogleSignInButton({ onSuccess }: GoogleSignInButtonProps) {
  const [loading, setLoading] = useState(false);

  const handleGoogleSignIn = async () => {
    setLoading(true);
    try {
      // En iOS Capacitor, window.location.origin es capacitor://localhost y Lovable/Google
      // no lo aceptan como redirect. Usamos la URL web configurada en Lovable.
      const redirectUri =
        Capacitor.isNativePlatform() && Capacitor.getPlatform() === "ios"
          ? WEB_APP_URL
          : window.location.origin;

      const result = await lovable.auth.signInWithOAuth("google", {
        redirect_uri: redirectUri,
      });

      if (result.redirected) {
        return; // Page is redirecting
      }

      if (result.error) {
        toast.error(result.error.message || "Error al iniciar sesión con Google");
        return;
      }

      onSuccess?.();
    } catch (error: any) {
      toast.error(error.message || "Error al iniciar sesión con Google");
    } finally {
      setLoading(false);
    }
  };

  return (
    <Button
      type="button"
      variant="outline"
      className="w-full h-12 text-base font-medium"
      onClick={handleGoogleSignIn}
      disabled={loading}
    >
      {loading ? (
        <Loader2 className="h-5 w-5 animate-spin" />
      ) : (
        <>
          <GoogleIcon />
          <span className="ml-2">Continuar con Google</span>
        </>
      )}
    </Button>
  );
}
